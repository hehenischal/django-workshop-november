# Numeric types in Python

# Integer
age = 25
print(type(age))
print("Age:", age)

# Float
height = 5.9
print("Height:", height)

# Complex
complex_number = 3 + 4j
print("Complex Number:", complex_number)










# Arithmetic operations
sum_result = age + height
print("Sum of age and height:", sum_result)

product_result = age * height
print("Product of age and height:", product_result)

# Using built-in functions
absolute_value = abs(-7)
print("Absolute value of -7:", absolute_value)

power_result = pow(2, 3)
print("2 to the power of 3:", power_result)

# Type conversion
int_to_float = float(age)
print("Integer to float:", int_to_float)

float_to_int = int(height)
print("Float to integer:", float_to_int)