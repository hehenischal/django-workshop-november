# Example of text type variables in Python

# String variable
greeting = "Hello, World!"

# Multi-line string variable
multi_line_text = """This is a multi-line string.
It can span multiple lines."""

# String concatenation
first_name = "John"
last_name = "Doe"
full_name = first_name + " " + last_name

# String formatting
age = 30
formatted_string = f"My name is {full_name} and I am {age} years old."

# String methods
upper_case_greeting = greeting.upper()
lower_case_greeting = greeting.lower()
title_case_greeting = greeting.title()

# Output the variables
print(greeting)
print(multi_line_text)
print(full_name)
print(formatted_string)
print(upper_case_greeting)
print(lower_case_greeting)
print(title_case_greeting)